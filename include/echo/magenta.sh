################################################################################
# Echo magenta color
################################################################################
function echo_magenta() {
  echo -en '\033[0;35m'
  echo $2 "${1}"
  echo -en '\033[0m'
}

function echo_magentaBold() {
  echo -en '\033[1;35m'
  echo $2 "${1}"
  echo -en '\033[0m'
}
